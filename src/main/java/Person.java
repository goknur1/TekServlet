public class Person {
    private String ad;
    private String soyAd;

    public Person(String ad, String soyAd) {
        this.ad = ad;
        this.soyAd = soyAd;
    }

    public String getAd() {
        return ad;
    }

    public void setAd(String ad) {
        this.ad = ad;
    }

    public String getSoyAd() {
        return soyAd;
    }

    public void setSoyAd(String soyAd) {
        this.soyAd = soyAd;
    }
}
